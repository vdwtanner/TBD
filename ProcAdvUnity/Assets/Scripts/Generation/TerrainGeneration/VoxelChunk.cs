    public IEnumerator generate(PerlinNoise heightMap, PerlinNoise roughness, int minHeight, int heightMul, int cliffHeightCutoff)
    {
        // lets take 2 samples of the perlin noise on each dimension like so:
        // | - - - - - - - - - - - - - - |
        // 0                             15

        PerlinSample[,,] samples = new PerlinSample[2, 2, 2];

        float roughThresh = 0.5f;

        // generate all our sample points
        for (int i = 0; i < 2; i++)
        {
            for (int j = 0; j < 2; j++)
            {
                for (int k = 0; k < 2; k++)
                {
                    Vector3 worldpoint = new Vector3(i * 15, j * 15, k * 15) + chunkPosition;
                    float heightVal = heightMap.FractalNoise2D(worldpoint.x, worldpoint.z, 4, 16, 1);
                    heightVal = (heightVal + 1) / 2;
                    float roughVal = roughness.FractalNoise3D(worldpoint.x, worldpoint.y, worldpoint.z, 8, 1, 1);
                    roughVal = (roughVal + 1) / 2;
                    PerlinSample s;
                    s.heightThresh = (heightVal * heightMul) + minHeight;
                    s.roughVal = roughVal;
                    float divisor = worldpoint.y - (s.heightThresh + cliffHeightCutoff);
                    if (divisor > 0)
                        s.roughVal /= divisor;
                    samples[i, j, k] = s;
                }
            }
        }

        // update all the chunkvoxels
        for (int i = 0; i < chunkWidth; i++)
        {
            for (int j = 0; j < chunkHeight; j++)
            {
                for (int k = 0; k < chunkDepth; k++)
                {
                    Vector3 worldpoint = new Vector3(i, j, k) + chunkPosition;

                    voxelArr[i + j * chunkWidth + k * chunkWidth * chunkHeight].pos = new Vector3(i, j, k);

                    int sx = (i / 16);
                    int sy = (j / 16);
                    int sz = (k / 16);

                    /*if (i % 15 == 0 && j % 15 == 0 && k % 15 == 0) {
                        if (worldpoint.y > samples[sx, sy, sz].heightThresh)
                            voxelArr[i + j * chunkWidth + k * chunkWidth * chunkHeight].health = 0.0f;
                        else
                            voxelArr[i + j * chunkWidth + k * chunkWidth * chunkHeight].health = 10.0f;

                        if (samples[sx, sy, sz].roughVal > roughThresh)
                            voxelArr[i + j * chunkWidth + k * chunkWidth * chunkHeight].health = 10.0f;

                        continue;
                    }*/

                    // the bottom square of our sample area
                    PerlinSample p0 = samples[sx, sy, sz];
                    PerlinSample p1 = samples[sx, sy, sz+1];
                    PerlinSample p2 = samples[sx+1, sy, sz+1];
                    PerlinSample p3 = samples[sx+1, sy, sz];

                    // the top square of our sample area
                    PerlinSample p4 = samples[sx, sy+1, sz];
                    PerlinSample p5 = samples[sx, sy+1, sz+1];
                    PerlinSample p6 = samples[sx+1, sy+1, sz+1];
                    PerlinSample p7 = samples[sx+1, sy+1, sz];

                    // lots of lerps
                    float hl01 = Mathf.Lerp(p0.heightThresh, p1.heightThresh, (k % 16) / 15.0f);
                    float hl32 = Mathf.Lerp(p3.heightThresh, p2.heightThresh, (k % 16) / 15.0f);
                    float hlbottom = Mathf.Lerp(hl01, hl32, (i % 16) / 15.0f);

                    //Debug.Log("heightThresh = " + hlbottom);

                    if (worldpoint.y > hlbottom)
                    {
                        voxelArr[i + j * chunkWidth + k * chunkWidth * chunkHeight].health = 0.0f;
                    }
                    else
                    {
                        voxelArr[i + j * chunkWidth + k * chunkWidth * chunkHeight].health = 10.0f;
                        continue;
                    }

                    float rl01 = Mathf.Lerp(p0.roughVal, p1.roughVal, (k % 16) / 15.0f);
                    float rl32 = Mathf.Lerp(p3.roughVal, p2.roughVal, (k % 16) / 15.0f);
                    float rlbottom = Mathf.Lerp(rl01, rl32, (i % 16) / 15.0f);

                    float rl45 = Mathf.Lerp(p4.roughVal, p5.roughVal, (k % 16) / 15.0f);
                    float rl76 = Mathf.Lerp(p7.roughVal, p6.roughVal, (k % 16) / 15.0f);
                    float rltop = Mathf.Lerp(rl45, rl76, (i % 16) / 15.0f);

                    float r = Mathf.Lerp(rlbottom, rltop, (j % 16) / 15.0f);

                    if (r > roughThresh)
                        voxelArr[i + j * chunkWidth + k * chunkWidth * chunkHeight].health = 10.0f;

                }
            }
        }

        terrain.StageForUpdate(this);

        generated = true;

        yield return null;
    }

    public void polygonize(float minHealth) {

        List<Vector3> positions = new List<Vector3>();
        List<Vector3> normals = new List<Vector3>();
        List<int> index = new List<int>();
        int idx = 0;

        for (int x = 0; x < chunkWidth-1; x++)
        {
            for (int y = 0; y < chunkHeight-1; y++)
            {
                for (int z = 0; z < chunkDepth-1; z++)
                {
                    TerrainVoxel v0 = voxelArr[x + y * chunkWidth + z * chunkWidth * chunkHeight];
                    TerrainVoxel v1 = voxelArr[(x + 1) + y * chunkWidth + z * chunkWidth * chunkHeight];
                    TerrainVoxel v2 = voxelArr[(x + 1) + (y + 1) * chunkWidth + z * chunkWidth * chunkHeight];
                    TerrainVoxel v3 = voxelArr[x + (y + 1) * chunkWidth + z * chunkWidth * chunkHeight];
                    TerrainVoxel v4 = voxelArr[x + y * chunkWidth + (z + 1) * chunkWidth * chunkHeight];
                    TerrainVoxel v5 = voxelArr[(x + 1) + y * chunkWidth + (z + 1) * chunkWidth * chunkHeight];
                    TerrainVoxel v6 = voxelArr[(x + 1) + (y + 1) * chunkWidth + (z + 1) * chunkWidth * chunkHeight];
                    TerrainVoxel v7 = voxelArr[x + (y + 1) * chunkWidth + (z + 1) * chunkWidth * chunkHeight];

                    int cubeIndex = 0;

                    if (v0.health >= minHealth) cubeIndex |= 1;
                    if (v1.health >= minHealth) cubeIndex |= 2;
                    if (v2.health >= minHealth) cubeIndex |= 4;
                    if (v3.health >= minHealth) cubeIndex |= 8;
                    if (v4.health >= minHealth) cubeIndex |= 16;
                    if (v5.health >= minHealth) cubeIndex |= 32;
                    if (v6.health >= minHealth) cubeIndex |= 64;
                    if (v7.health >= minHealth) cubeIndex |= 128;

                    if (cubeEdgeFlags[cubeIndex] == 0) continue;

                    Vector3[] verts = new Vector3[12];

                    if ((cubeEdgeFlags[cubeIndex] & 1) != 0)
                        verts[0] = Vector3.Lerp(v0.pos, v1.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 2) != 0)
                        verts[1] = Vector3.Lerp(v1.pos, v2.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 4) != 0)
                        verts[2] = Vector3.Lerp(v2.pos, v3.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 8) != 0)
                        verts[3] = Vector3.Lerp(v3.pos, v0.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 16) != 0)
                        verts[4] = Vector3.Lerp(v4.pos, v5.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 32) != 0)
                        verts[5] = Vector3.Lerp(v5.pos, v6.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 64) != 0)
                        verts[6] = Vector3.Lerp(v6.pos, v7.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 128) != 0)
                        verts[7] = Vector3.Lerp(v7.pos, v4.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 256) != 0)
                        verts[8] = Vector3.Lerp(v4.pos, v0.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 512) != 0)
                        verts[9] = Vector3.Lerp(v5.pos, v1.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 1024) != 0)
                        verts[10] = Vector3.Lerp(v6.pos, v2.pos, 0.5f);
                    if ((cubeEdgeFlags[cubeIndex] & 2048) != 0)
                        verts[11] = Vector3.Lerp(v7.pos, v3.pos, 0.5f);

                    for (int i = 0; triangleConnectionTable[cubeIndex, i] != -1; i += 3)
                    {
                        Vector3 p0 = verts[triangleConnectionTable[cubeIndex, i]];
                        Vector3 p1 = verts[triangleConnectionTable[cubeIndex, i + 1]];
                        Vector3 p2 = verts[triangleConnectionTable[cubeIndex, i + 2]];

                        Vector3 u = p1 - p0;
                        Vector3 v = p2 - p0;

                        Vector3 norm = -Vector3.Normalize(Vector3.Cross(u, v));

                        positions.Add(p2);
                        positions.Add(p1);
                        positions.Add(p0);

                        normals.Add(norm);
                        normals.Add(norm);
                        normals.Add(norm);

                        index.Add(idx++);
                        index.Add(idx++);
                        index.Add(idx++);
                    }
                }
            }
        }

        if (positions.Count > 0)
            UnityEngine.Debug.Log("Count is " + positions.Count);

        mesh.Clear();
        mesh.vertices = positions.ToArray();
        //mesh.normals = normals.ToArray();
        mesh.bounds = new Bounds(new Vector3(chunkWidth / 2, chunkHeight / 2, chunkDepth / 2), new Vector3(chunkWidth, chunkHeight, chunkDepth));
        mesh.SetTriangles(index.ToArray(), 0);

        mesh.RecalculateNormals();

        meshGob.GetComponent<MeshCollider>().sharedMesh = mesh;
    }

